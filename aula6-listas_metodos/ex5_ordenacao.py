# Lista com 7 números inteiros
numeros = [34, 12, 5, 67, 23, 89, 1]

# Lista em ordem crescente usando sort()
numeros.sort()
print("Números em ordem crescente:", numeros)

# Lista em ordem decrescente usando sort() com reverse=True
numeros.sort(reverse=True)
print("Números em ordem decrescente:", numeros)

# Exibindo o maior e o menor número usando max() e min()
print("Maior número:", max(numeros))
print("Menor número:", min(numeros))